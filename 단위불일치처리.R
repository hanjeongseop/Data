# -----------------------------------
# 1. 실습 데이터 생성
# -----------------------------------
df <- data.frame(
  LOT = c("L01", "L02", "L03", "L04", "L05", "L06", "L07", "L08"),
  제품 = c("A제품", "A제품", "A제품", "A제품", "B제품", "B제품", "B제품", "B제품"),
  투입량 = c(500, 0.52, 480, 0.51, 0.60, 620, 0.58, 590),
  단위 = c("g", "kg", "g", "kg", "kg", "g", "kg", "g"),
  stringsAsFactors = FALSE
)

# 데이터 확인
print("원본 데이터")
print(df)

# -----------------------------------
# 2. 단위를 무시한 잘못된 평균 계산
# -----------------------------------
wrong_avg <- aggregate(투입량 ~ 제품, data = df, FUN = mean)

print("단위를 무시한 제품별 평균 (잘못된 결과)")
print(wrong_avg)

# -----------------------------------
# 3. 단위를 g로 통일
# -----------------------------------
df$투입량_g <- ifelse(df$단위 == "kg", df$투입량 * 1000, df$투입량)

print("단위를 g로 통일한 데이터")
print(df)

# -----------------------------------
# 4. 단위 통일 후 올바른 평균 계산
# -----------------------------------
correct_avg <- aggregate(투입량_g ~ 제품, data = df, FUN = mean)

print("단위 통일 후 제품별 평균 (올바른 결과)")
print(correct_avg)

# -----------------------------------
# 5. 잘못된 결과와 올바른 결과 비교
# -----------------------------------
comparison <- merge(wrong_avg, correct_avg, by = "제품")
colnames(comparison) <- c("제품", "단위무시_평균", "단위통일후_평균_g")

print("비교 결과")
print(comparison)

# -----------------------------------
# 6. 시각화: 단위 통일 후 제품별 평균 막대그래프
# -----------------------------------
barplot(
  height = correct_avg$투입량_g,
  names.arg = correct_avg$제품,
  main = "제품별 평균 투입량 (g 기준)",
  xlab = "제품",
  ylab = "평균 투입량 (g)",
  ylim = c(0, max(correct_avg$투입량_g) + 100)
)

# -----------------------------------
# 7. 단위별 데이터 개수 확인
# -----------------------------------
unit_count <- table(df$단위)

print("단위별 데이터 개수")
print(unit_count)

# -----------------------------------
# 8. 제품별 + 단위별 데이터 분포 확인
# -----------------------------------
cross_tab <- table(df$제품, df$단위)

print("제품별 단위 분포")
print(cross_tab)

